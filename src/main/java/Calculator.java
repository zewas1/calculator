public class Calculator {

    public static int Sum(int x, int j) {
        return x + j;
    }

    public static int Subtract(int x, int j) {
        return x + j;
    }

    public static double Multiplication(double x, double j) {
        return x * j;
    }

    public static double Division(double x, double j) {
        return x / j;
    }
}
